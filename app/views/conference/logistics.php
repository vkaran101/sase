  <div class="row body-row">
    <center><h3>Getting Here</h3></center>
      
    <p><b>From the north (via Route I-93 or Route 1)</b> <br>
    Take the Storrow Drive exit, and proceed to the Fenway exit. Follow signs for Boylston Street inbound, and bear right onto Westland Avenue. Turn right onto Massachusetts Avenue, proceed to the third traffic light, and turn right onto Columbus Avenue. The Renaissance Parking Garage is at 835 Columbus Avenue.</p>
    <p><b>From the west (via Route I-90, Massachusetts Turnpike)</b> <br>
    Take Exit 22 (Copley Square), and bear right. Proceed to the first traffic light, and turn right onto Dartmouth Street. Take the next right onto Columbus Avenue. The Renaissance Parking Garage is at 835 Columbus Avenue.</p>
    <p><b>From the west (via Route 9)</b> <br>
    Proceed east on Route 9; it will become Huntington Avenue. Turn right onto Ruggles Street. At the fourth traffic light, turn left onto Tremont Street. At the second set of lights, turn left onto Melnea Cass Boulevard, and then turn left onto Columbus Avenue. The Renaissance Parking Garage is at 835 Columbus Avenue.</p>
    <p><b>From the south (via I-93, Route 3)</b> <br>
    Take Exit 18 (Massachusetts Avenue/Roxbury/Frontage Road). Turn left at the third light, staying in one of the two left lanes. Proceed straight onto Melnea Cass Boulevard. Continue for approximately two miles and turn left onto Columbus Avenue. The Renaissance Parking Garage is at 835 Columbus Avenue.</p>
    <p><b>From Logan Airport</b> <br>
    Exiting Logan Airport, merge onto Route I-90 West/Mass Pike/Ted Williams Tunnel. Proceed about 1.5 miles, and then take Exit 24 toward Route I-93. Merge onto Route I-93 South via the exit on the left. Take Exit 18, Massachusetts Avenue, toward Andrew Square. Bear right onto the Massachusetts Avenue connector. Proceed straight on the connector, which becomes Melnea Cass Boulevard. Continue for approximately two miles and turn left onto Columbus Avenue. The Renaissance Parking Garage is at 835 Columbus Avenue.</p>
    <p><b>From Manchester (NH)-Boston Regional Airport</b> <br>
    Via automobile:<br>
    Exiting the airport, follow the signs to I-93 South, then follow driving
    directions to campus from the North via I-93. <br>
    <br>
    Via public transportation:<br>
    The Manchester Airport Shuttle is a regularly scheduled bus service offered
    free of charge for Manchester-Boston Regional Airport's ticketed airline
    passengers. All passengers must show an airline ticket, boarding pass, or
    printed travel itinerary into or out of Manchester to be eligible to use
    this free service.<br>
    Buses depart from the outer curb adjacent to the main entrance walkway of
    the terminal. The bus service operates every two hours, 24-hours a day,
    seven days a week (including holidays), between the airport and the
    Sullivan Square MBTA Subway Station.<br>
    At the Sullivan Square station, take the MBTA Orange Line (Inbound towards
    Forest Hills), getting off at Ruggles Station.</p>
    <p><b>Via public transportation</b> <br>
    Northeastern is accessible by subway via the Green Line of the MBTA. From downtown Boston, take an “E” train outbound to the Northeastern stop, the first stop above ground. The campus can also be reached from downtown via the Orange Line by taking any train going outbound to Forest Hills and getting off at Ruggles Station. Commuter rail lines connect with the Orange Line at Ruggles Station, Back Bay Station, and North Station.</p>

