  <div class="row body-row">
    <center><h3>Workshops</h3></center>

    <ol class="accordion" data-accordion>
      <li class="accordion-navigation">
        <a href="#breakout1">
          <b>Breakout 1 - Collaborate</b>
          <br><i>Learn the importance of working in a team of people with diverse skills and experiences to solve multifaceted problems.</i>
        </a>
        <div id="breakout1" class="content">
          <b>The Road Not Taken: The Many Destinations of the STEM Degree</b><br>
          <i>NAAAP and SASE Pro, moderated by George Luo; SN 108</i>
          <br>75% of US News' 100 Best Jobs of 2016 are located in STEM-related fields. With such an overwhelming array of industries, professions, and careers to choose from, how can one possibly choose?
          <br>Sit down with our selection of early-to-mid career professionals from a variety of different STEM fields and have a candid conversation about what their job is like. How did they choose their job? What do they enjoy the most about where they work? And what makes them itch to leave?
          <br>We hope that not only will you carry away the right answers to your career questions, but that you will carry away the right questions to ask as you find your own path "less traveled by."
          <hr>
          <b>Success in Corporate R&D</b><br>
          <i>Boston Scientific; RB 107</i>
          <br>This workshop will focus on finding success in corporate R&D, through focusing efforts where they will deliver the most impactful results. Learn more about developing your expertise, finding your niche, and adding value. Also learn more about the process of developing new product, working with the realities of cost, performance, and schedule limitations. We will also touch upon the product development pipeline, starting with broad concepts, and progressing through downselection procedures. 
          <hr>
          <b>6E’s: Redefine your leadership</b><br>
          <i>Irene Ng, Virak Ngauv; McLeod Suites (CSC 318,320,322)</i>
          <br>You probably have heard of or taken other leadership assessments, or models out there, such as the Myers-Briggs Type Indicator or Strengthsfinders. However, what makes our LSA (Leadership Self Assessment) different, is that we focus on continuous reflection and direct feedback from others to help track the growth of your leadership development. With the SASE 6E leadership model (Empower, Energize, Envision, Engage, Execute, Elastic), you can redefine and recognize the way you see leadership, as well as identify which of your leadership styles are stronger than others. Because when these 6 powers combine, they create senSASEtional leaders! 
          <hr>
          <b>The "Everything Else": At the Intersection of Social Justice and Technology</b><br>
          <i>ECAASU; EL 312</i>
          <br>Innovations in STEM have introduced unprecedented changes in our world and our communities. While we celebrate such breakthroughs, we as people invested in STEM also need to understand the potential repercussions that our work can cumulatively have on our world and our communities. In this session, we will discuss the "everything else": instances where politics, economics, finance, ethics, disparities, equity, and public policy intersect with STEM. By examining case studies and the themes they exemplify, this session will bring a stronger awareness of our impacts, good and bad, as we continue to make progress in STEM.
          <hr>
          <b>Optimizing your Team</b><br>
          <i>US Army; DG 150</i>
          <br>What can you do to create a successful team? The most important element in starting a team is understanding your best quality that you can bring forward to the group. This workshop will teach about group mentality, and how a team can succeed.
        </div>
      </li>
      <li class="accordion-navigation">
        <a href="#breakout2">
          <b>Breakout 2 - Create</b>
          <br><i>Participate in interactive workshops that encourage creativity, independent thinking and coming up with original ideas.</i>
        </a>
        <div id="breakout2" class="content">
          <b>Auditing Algorithms: Towards Transparency in the Age of Big Data</b><br>
          <i>Prof. Christo Wilson; DG 050</i>
          <br>In recent years, there has been growing awareness of powerful algorithms that mediate our daily lives. For example,  Facebook personalizes the information shown to each user. While making our lives much simpler, opaque algorithms can lead to harmful outcomes. In this workshop, we will discuss our work towards "auditing algorithms", where we use carefully controlled experiments to understand the data and algorithms used by companies such as Google and Uber, increase transparency, and inform societal discussions about how our personal data is being collected and used.
          <hr>
          <b>3D Printing Workshop</b><br>
          <i>Jiaqing Qiu; SN 368</i>
          <br>The popularity and awareness of 3D Printing is exploding. It is breaking down barriers in design and manufacturing, and unlock the potential in every person to create, innovate and fabricate 
          <br>In this workshop we'll give basic understanding: How to create 3D geometry; how does 3D-printer work; what are the different types of printers and materials to use; what can I print with a 3D-printer; what is 3D printing limitations;
          <br>Participants will gain exposure to the main facts of initializing and set up the 3D Printer with basic settings during the workshop, 3D modeling, and 3D printing during the workshop.
          <hr>
          <b>Early-Stage Advice for New Startup Founders</b><br>
          <i>MassChallenge; McLeod Suites (CSC 318,320,322)</i>
          <br>Join MassChallenge and several MassChallenge alumni founders to discuss some common challenges to new founders. This is a great opportunity to ask questions of entrepreneurs who have been through (and are still going through!) ups and downs of starting a startup. How do you build a team? Should you apply to an accelerator? How do you gain momentum before launching a product? Find out more in this interactive panel.
          <hr>
          <b>Product Development</b><br>
          <i>Fikst Product Development; SN 108</i>
          <br>Take a detailed look into the product development process, and what each stage of the project requires from its team. The workshop will walk through Fikst's product design process, with specific examples of work done by Fikst Product Development. Emphasis will be placed on creating prototypes early and often to learn quickly and iterate on design concepts.
